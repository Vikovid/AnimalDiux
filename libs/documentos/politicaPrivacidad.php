				derecho a la protección de sus datos personales.
			</p>
			<p>Esta Política de Privacidad describe como recopilamos, recibimos, usamos,
				almacenamos, compartimos y procesamos sus Datos Personales. A los efectos
				de esta Política de Privacidad, “Datos Personales” significa cualquier
				información relacionada con una persona física o identificable. Si no está de
				acuerdo con esta Política de Privacidad, no debe usar el servicio que contiene
				un enlace a esta Política de Privacidad.
			</p>
			<p>Actualizaremos esta Política de Privacidad de vez en cuando y se hará de su
				conocimiento mediante la publicación de éste en los sitios en donde se tenga
				un enlace a esta Política de Privacidad; la cual, estará disponible las 24 horas
				los 365 días del año.
			</p>
